import math

# 1. Ввод переменных
xb = float(input("Введите X начальное: "))
xe = float(input("Введите X конечное: "))

dx = float(input("Введите шаг (dx): "))
# Проверка dx
while dx <= 0:
    print("Ошибка: dx должен быть больше нуля.")
    dx = float(input("Введите шаг (dx): "))

eps = float(input("Введите точность (eps): "))
# Проверка eps
while eps <= 0:
    print("Ошибка: eps должен быть больше нуля.")
    eps = float(input("Введите точность (eps): "))

# 2. Вывод "шапки" таблицы
header = "+----------+------------+------------+"
print("\nТаблица значений arcctg(x) через ряд:")
print(header)
print("|    Xt    |  arcctg(x) | Кол-во (n) |")
print(header)

# 3. Инициировать Xt
xt = xb

# 4. Внешний цикл по Xt
while xt <= xe + dx / 1000:

    # 5. Инициализация для суммы ряда arcctg(x)
    s = math.pi / 2  # Начальная константа из формулы
    an = -xt  # Первый член ряда (при n=0)
    n = 0

    # 6. Внутренний цикл по an (поиск суммы с заданной точностью)
    while True:
        s = s + an

        # 8. Проверка точности ПЕРЕД вычислением следующего шага
        if abs(an) < eps:
            break

        # 7. Вычисление коэффициента k и следующего члена ряда
        k = -(xt ** 2 * (2 * n + 1)) / (2 * n + 3)
        an = an * k
        n = n + 1

        # Защита от бесконечного цикла
        if n > 100000:
            break

    # 9. Вывод строки таблицы
    print("| {0:8.2f} | {1:10.6f} | {2:10d} |".format(xt, s, n))

    # 10. Шаг по X
    xt = xt + dx

print(header)